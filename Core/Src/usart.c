void F_UART_Send(char * buff, uint16_t length){
	uint16_t l_cnt=0;

	while((buff[l_cnt]!='\0') && (l_cnt<length)) l_cnt++;

	HAL_UART_Transmit(&huart4,buff, l_cnt, 100);
}
